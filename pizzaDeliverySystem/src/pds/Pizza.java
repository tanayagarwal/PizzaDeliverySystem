package pds;
import java.io.*;
import java.util.*;
public class Pizza {
	int pId;
	String pName;
	double pPrice;
	String pType;
	String pSize;
	String pBase;
	
	public int getpId() {
		return pId;
	}
	public void setpId(int pId) {
		this.pId = pId;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public double getpPrice() {
		return pPrice;
	}
	public void setpPrice(double pPrice) {
		this.pPrice = pPrice;
	}
	public String getpType() {
		return pType;
	}
	public void setpType(String pType) {
		this.pType = pType;
	}
	public String getpSize() {
		return pSize;
	}
	public void setpSize(String pSize) {
		this.pSize = pSize;
	}
	public String getpBase() {
		return pBase;
	}
	public void setpBase(String pBase) {
		this.pBase = pBase;
	}
	
}
